package org.example.factorymethod.cakes;

public class LemonDrizzle {
  public void Bake()
  {
    System.out.println("Baking Lemon Drizzle for 15 minutes");
  }

  public void GetIngredients()
  {
    System.out.println("Lemon Drizzle Ingredients Acquired");
  }

  public void MixIngredients()
  {
    System.out.println("Lemon Drizzle Ingredients Mixed");
  }

  public void PutIngredientsInTin()
  {
    System.out.println("Lemon Drizzle Ingredients Scooped into Loaf tin. Then Drizzled with lemon sugar");
  }
}
